/**
 * Position File Writer
 *
 * Persists cs-pos-* element overrides to:
 *   1. A world-level game setting (source of truth, survives rebuilds)
 *   2. A <style> tag injected into document.head (takes effect immediately)
 *
 * Note: Foundry's FilePicker.upload() blocks .css files, so CSS is injected
 * directly via a <style> tag rather than written to disk. The game setting
 * provides full cross-session persistence without needing a file.
 *
 * Only handles elements with a unique cs-pos-* class. Generic cs-ability / cs-save
 * elements share the same class across multiple instances and cannot be targeted by
 * a single CSS rule, so they continue to use inline styles only.
 */

const MODULE_ID    = 'osp-houserules';
const SETTING_KEY  = 'positionOverrides';
const STYLE_TAG_ID = 'osp-position-overrides';

// ── Setting registration ───────────────────────────────────────────────────

export function registerPositionSettings() {
  game.settings.register(MODULE_ID, SETTING_KEY, {
    name: 'Position Overrides',
    hint: 'Auto-generated by the position tool. Do not edit manually.',
    scope: 'world',
    config: false,
    type: Object,
    default: {}
  });
}

// ── Public API ─────────────────────────────────────────────────────────────

/**
 * Save a new position for a cs-pos-* element and apply it immediately.
 * No-ops silently for non-cs-pos-* classes.
 */
export async function savePositionOverride(cssClass, left, top, width, height) {
  if (!cssClass?.startsWith('cs-pos-')) return;

  const overrides = _getOverrides();
  overrides[cssClass] = { left, top, width, height };
  await game.settings.set(MODULE_ID, SETTING_KEY, overrides);
  await _apply(overrides);
}

/**
 * Remove a saved override for a cs-pos-* element and revert to stylesheet default.
 */
export async function removePositionOverride(cssClass) {
  if (!cssClass?.startsWith('cs-pos-')) return;

  const overrides = _getOverrides();
  delete overrides[cssClass];
  await game.settings.set(MODULE_ID, SETTING_KEY, overrides);
  await _apply(overrides);
}

/**
 * Load and apply any stored overrides. Call this from the ready hook.
 */
export async function applyStoredPositionOverrides() {
  const overrides = _getOverrides();
  if (Object.keys(overrides).length > 0) {
    await _apply(overrides);
  }
}

// ── Internal helpers ───────────────────────────────────────────────────────

function _getOverrides() {
  try {
    return foundry.utils.deepClone(game.settings.get(MODULE_ID, SETTING_KEY)) ?? {};
  } catch (e) {
    return {};
  }
}

async function _apply(overrides) {
  const css = _generateCSS(overrides);
  _injectStyleTag(css);
}

function _generateCSS(overrides) {
  const lines = ['/* OSP Position Overrides — auto-generated by position tool. Do not edit manually. */'];
  for (const [cssClass, pos] of Object.entries(overrides)) {
    lines.push(`.${cssClass} { --left: ${pos.left}px; --top: ${pos.top}px; --width: ${pos.width}px; --height: ${pos.height}px; }`);
  }
  return lines.join('\n');
}

function _injectStyleTag(css) {
  try {
    let el = document.getElementById(STYLE_TAG_ID);
    if (!el) {
      el = document.createElement('style');
      el.id = STYLE_TAG_ID;
      (document.head ?? document.documentElement).appendChild(el);
    }
    el.textContent = css;
  } catch (err) {
    console.error('OSP | Failed to inject position overrides style tag:', err);
  }
}

